# MAGIC - Which product category generated the highest total sales revenue, and what percentage of overall revenue did it contribute?

# COMMAND ----------


# Total revenue by product category
category_revenue = (
    df.groupby("product_category")["revenue"]
      .sum()
      .reset_index()
)

# Sort from highest to lowest revenue
category_revenue = category_revenue.sort_values(by="revenue", ascending=False)

# Overall revenue
total_revenue = category_revenue["revenue"].sum()

# Calculate percentage contribution
category_revenue["percentage_contribution"] = (
    category_revenue["revenue"] / total_revenue * 100
).round(2)

# Display all categories
print(category_revenue)

# Display the top category
top_category = category_revenue.iloc[0]

print("\nHighest Revenue Category")
print(f"Category: {top_category['product_category']}")
print(f"Revenue: {top_category['revenue']:.2f}")
print(f"Percentage of Total Revenue: {top_category['percentage_contribution']:.2f}%")

