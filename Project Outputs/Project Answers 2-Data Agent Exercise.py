# MAGIC **Question 3**:
# MAGIC - Which store location had the highest sales revenue, and how does it compare with the lowest-performing store

# COMMAND ----------

# Ensure revenue is numeric
df["revenue"] = pd.to_numeric(df["revenue"], errors="coerce")

# Calculate total revenue by store
store_revenue = (
    df.groupby("store_location")["revenue"]
      .sum()
      .reset_index()
      .sort_values(by="revenue", ascending=False)
)

# Highest-performing store
highest_store = store_revenue.iloc[0]

# Lowest-performing store
lowest_store = store_revenue.iloc[-1]

# Calculate the difference
revenue_difference = highest_store["revenue"] - lowest_store["revenue"]

# Calculate percentage difference
percentage_difference = (
    (revenue_difference / lowest_store["revenue"]) * 100
)

# Display all store revenues
print(store_revenue)

print("\nHighest-Performing Store")
print(f"Store: {highest_store['store_location']}")
print(f"Revenue: {highest_store['revenue']:.2f}")

print("\nLowest-Performing Store")
print(f"Store: {lowest_store['store_location']}")
print(f"Revenue: {lowest_store['revenue']:.2f}")

print(f"\nRevenue Difference: {revenue_difference:.2f}")
print(f"Percentage Difference: {percentage_difference:.2f}%")

